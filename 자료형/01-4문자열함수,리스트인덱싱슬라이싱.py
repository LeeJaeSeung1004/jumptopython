#문자열 함수
# 문자열 자료형은 자체적으로 함수를 가지고 있다. 이것을 문자열 내장 함수라 한다.
# 내장 함수를 사용하려면 변수.함수명으로 사용한다

#문자 개수 세기(count)
a = "hobby"
a.count("b")
#문자열 지정문자의 개수를 돌려준다

#문자의 위치찾기1(find)
a = "python is the best choice"
a.find("b")
a.find("k")
# 문자열중 입력한 문자가 처음 나온 위치를 반환한다
# 찾지못하면 -1을 반환한다

#문자의 위치찾기2(index)
a = "Life is too short"
a.index("t")
a.index("k")

